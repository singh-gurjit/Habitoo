import SwiftUI

class ColorUtil {
    
    func getlightGrayColor() -> Color {
        return Color(red: 248 / 255, green: 248 / 255, blue: 248 / 255)
    }
    
    func getAccentColor() -> Color {
        return Color(red: 244 / 255, green: 118 / 255, blue: 94 / 255)
    }
    
}
